/*用户表*/
CREATE TABLE user(
    uid INT PRIMARY KEY AUTO_INCREMENT,/*用户编号*/
    uname VARCHAR(32),      /*邮箱*/
    phone VARCHAR(11),      /*手机号*/
    upwd VARCHAR(32),       /*密码*/
    nickname VARCHAR(32),   /*昵称*/
    sex VARCHAR(1),         /*性别,男-M，女-F*/
    age VARCHAR(3),         /*年龄*/
    edu VARCHAR(32),        /*学历*/
    job VARCHAR(32)         /*职业*/
);
