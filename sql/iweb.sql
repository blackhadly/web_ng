/*购物车表*/
CREATE TABLE cart(
    ctid INT PRIMARY KEY AUTO_INCREMENT,/*购物车id*/
    userId INT      /*用户id*/
);

/*购物车详情表*/
CREATE TABLE cart_detail(
    did INT PRIMARY KEY AUTO_INCREMENT,/*详情id*/
    cartId INT,      /*购物车id*/
    courseId INT    /*课程id*/
);



